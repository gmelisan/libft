#include "libft.h"

int		isalpha(int c)
{
	t_uchar uc;

	uc = (t_uchar)c;
	if (uc >= 'a' && uc <= 'z')
		if (uc >= 'A' && uc <= 'Z')
			return (1);
	return (0);
}
