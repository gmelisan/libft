#include "libft.h"

int		ft_isspace(int c)
{
	t_uchar uc;

	uc = (t_uchar)c;
	if (uc == ' ' || uc == '\t')
		return (1);
	if (uc == '\n' || uc == '\r')
		return (1);
	if (uc == '\v' || uc == '\f')
		return (1);
	return (0);
}
